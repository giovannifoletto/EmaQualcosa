function createHover(a) {
    /*
        Questa funzione serve a gestire gli eventi quando si passa sopra la provincia
        - cambiamento di opacit√† per il colore della regione
        - funzione popover con timeout ?
    */
    var active;
    $(a.id).hover(
        function() {
            if (a.id != active) {
                $(active).popover('hide');
            }
            active = a.id;
            showPopover(a);
            $(this).css('opacity', '50%');
        },
        function() {
            $(this).css("opacity", "100%");
            if (active == a.id) {
                $(a.id).popover('hide');
            }
        }
    );
}

