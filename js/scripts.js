function inizializeInput() {
    var s = "";
    $("#searchBar").keypress(e => {
        if (e.which == 13) {
            eventSubmit(s);
            s = "";
            $("#searchBar").val("");
        } else {
            s += String.fromCharCode(e.which);
        }
    });
}
